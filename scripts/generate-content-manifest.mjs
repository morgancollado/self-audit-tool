#!/usr/bin/env node
// Generates the broker content manifest (lib/content/brokers.ts) from the JSON
// files under content/brokers/**. The static export can't read the filesystem at
// runtime, so the manifest is a committed barrel of explicit `import` statements
// (the same shape lib/content/laws.ts hand-maintains) — only here it's generated,
// so adding a broker is just dropping a JSON file and re-running this.
//
// Run:   npm run generate:content
// CI:    regenerates and fails if the committed manifest is stale (drift guard),
//        so a forgotten regen can never ship a broker the app doesn't load.

import { readdirSync, statSync, writeFileSync, readFileSync } from 'node:fs';
import { join } from 'node:path';

const BROKER_DIR = 'content/brokers';
const OUT = 'lib/content/brokers.ts';

function walk(dir, acc = []) {
  for (const entry of readdirSync(dir)) {
    const p = join(dir, entry);
    if (statSync(p).isDirectory()) walk(p, acc);
    else if (p.endsWith('.json')) acc.push(p);
  }
  return acc;
}

// Deterministic order (by path) so the generated file is stable across machines
// and the CI drift check is meaningful.
const files = walk(BROKER_DIR).sort();

// Import paths are relative to lib/content/ (where the manifest lives).
const lines = [
  '// AUTO-GENERATED by scripts/generate-content-manifest.mjs — do not edit by hand.',
  '// Run `npm run generate:content` after adding or removing a broker JSON file.',
  '// Source of truth: content/brokers/**/*.json',
  '',
  "import { Broker } from './types';",
  '',
];

files.forEach((f, i) => {
  // content/brokers/us/spokeo.json -> ../../content/brokers/us/spokeo.json
  lines.push(`import b${i} from '../../${f}';`);
});

lines.push('');
lines.push('// Cast at the single chokepoint: the CI content validator (lib/content/validate.mjs)');
lines.push('// is the real schema gate; a data-integrity unit test asserts the safety invariants.');
lines.push(
  `export const BROKERS = [${files.map((_, i) => `b${i}`).join(', ')}] as unknown as Broker[];`,
);
lines.push('');

const next = lines.join('\n');
const prev = (() => {
  try {
    return readFileSync(OUT, 'utf8');
  } catch {
    return null;
  }
})();

if (prev === next) {
  console.log(`[manifest] ${OUT} already up to date (${files.length} broker(s)).`);
} else {
  writeFileSync(OUT, next);
  console.log(`[manifest] wrote ${OUT} (${files.length} broker(s)).`);
}
